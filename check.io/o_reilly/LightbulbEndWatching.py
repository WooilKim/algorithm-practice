import math
from datetime import datetime
from typing import List, Optional


def sum_light(els: List[datetime], start_watching: Optional[datetime] = None,
              end_watching: Optional[datetime] = None) -> int:
    start_watching = start_watching.timestamp() if start_watching else 0
    end_watching = end_watching.timestamp() if end_watching else math.inf
    ans = 0
    for i in range(0, len(els), 2):
        if len(els) > i + 1:
            t1, t2 = els[i].timestamp(), els[i + 1].timestamp()
            ans += max(min(t2, end_watching) - max(t1, start_watching), 0)
        else:
            t1 = els[i].timestamp()
            ans += max(end_watching - max(t1, start_watching), 0)
    return ans


